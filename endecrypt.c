void rotate(unsigned char *in) {
    unsigned char a,c;
    a = in[0];
    for(c=0;c<3;c++) 
        in[c] = in[c + 1];
    in[3] = a;
}


void rotate_block(int b, int r, int n) {
    unsigned char temp;
    for (int i = 0; i < n; i++) {
        temp = blocks[b].data[r][0];
        for (int j = 0; j < 3; j++) {
            blocks[b].data[r][j] = blocks[b].data[r][j + 1];
        }
        blocks[b].data[r][3] = temp;
    }
}


void rotate_block_inv(int b, int r, int n) {
    unsigned char temp;
    for (int i = 0; i < n; i++) {
        temp = blocks[b].data[r][3];
        for (int j = 3; j > 0; j--) {
            blocks[b].data[r][j] = blocks[b].data[r][j - 1];
        }
        blocks[b].data[r][0] = temp;
    }
}


void expand_key() {
    int i, j, k, c;
    unsigned char t[4];

    for (i = 0; i < 4; i++) {
        for (j = 0; j < 4; j++) {
            expanded_key[i * 4 + j] = key.data[j][i];
        }
    }

    for (i = 16; i <= 176; i += 4) {
        for (j = 0; j < 4; j++)
            t[j] = expanded_key[i + j - 4];
        
        if (i % 16 == 0) {
            rotate(t);
            for (k = 0; k < 4; k++) {
                t[k] = s_box[t[k]];
                if (k == 0) t[k] = t[k] ^ rcon[i / 16];
            }
        }

        c = 0;
        for (k = i - 16; k < i - 12; k++) {
            expanded_key[i + c] = expanded_key[k] ^ t[c];
            c++;
        }
    
    }
}


void update_key(int round) {
    unsigned char round_key[16];
    for (int i = 0; i < 16; i++)
        round_key[i] = expanded_key[16 * round + i];

    set_key(round_key);
}


unsigned char gmul(unsigned char a, unsigned char b) {
	unsigned char p = 0;
	unsigned char counter;
	unsigned char hi_bit_set;
	for(counter = 0; counter < 8; counter++) {
		if((b & 1) == 1) 
			p ^= a;
		hi_bit_set = (a & 0x80);
		a <<= 1;
		if(hi_bit_set == 0x80) 
			a ^= 0x1b;
		b >>= 1;
	}
	return p;
}


void gmix_column(unsigned char *r) {
    unsigned char a[4];
    unsigned char b[4];
    unsigned char c;
    unsigned char h;
    /* The array 'a' is simply a copy of the input array 'r'
     * The array 'b' is each element of the array 'a' multiplied by 2
     * in Rijndael's Galois field
     * a[n] ^ b[n] is element n multiplied by 3 in Rijndael's Galois field */ 
    for (c = 0; c < 4; c++) {
        a[c] = r[c];
        /* h is 0xff if the high bit of r[c] is set, 0 otherwise */
        h = (r[c] >> 7) & 1; /* arithmetic right shift, thus shifting in either zeros or ones */
        b[c] = r[c] << 1; /* implicitly removes high bit because b[c] is an 8-bit char, so we xor by 0x1b and not 0x11b in the next line */
        b[c] ^= h * 0x1B; /* Rijndael's Galois field */
    }
    r[0] = b[0] ^ a[3] ^ a[2] ^ b[1] ^ a[1]; /* 2 * a0 + a3 + a2 + 3 * a1 */
    r[1] = b[1] ^ a[0] ^ a[3] ^ b[2] ^ a[2]; /* 2 * a1 + a0 + a3 + 3 * a2 */
    r[2] = b[2] ^ a[1] ^ a[0] ^ b[3] ^ a[3]; /* 2 * a2 + a1 + a0 + 3 * a3 */
    r[3] = b[3] ^ a[2] ^ a[1] ^ b[0] ^ a[0]; /* 2 * a3 + a2 + a1 + 3 * a0 */
}


void gmix_column_inv(unsigned char *r) {
    unsigned char a[4];
    unsigned char c;

    for (c = 0; c < 4; c++) {
        a[c] = r[c];
    }
    // galois field multiplication and addition
    r[0] = gmul(a[0],14) ^ gmul(a[1],11) ^ gmul(a[2],13) ^ gmul(a[3],9);
    r[1] = gmul(a[0],9) ^ gmul(a[1],14) ^ gmul(a[2],11) ^ gmul(a[3],13);
    r[2] = gmul(a[0],13) ^ gmul(a[1],9) ^ gmul(a[2],14) ^ gmul(a[3],11);
    r[3] = gmul(a[0],11) ^ gmul(a[1],13) ^ gmul(a[2],9) ^ gmul(a[3],14);
}


void xor_(int b, int r, unsigned char* key) {
    for (int i = 0; i < 4; i++) {
        blocks[b].data[r][i] ^= key[i];
    }
}


void set_key(unsigned char* key_in) {
    for (int i = 0; i < 16; i++) {
        key.data[i % 4][(int)(i / 4)] = key_in[i];
    }
}


void blockify(unsigned char* data, int length, bool is_encrypting) {
    // PKCS#7 padding
    int padding = 16 - (length % 16);
    num_of_blocks = ceil(length / 16.0);
    // If text is completely divisible by 16, add an extra block anyways
    if (length % 16 == 0 && is_encrypting) {
        num_of_blocks++;
    }
    blocks = (Block*)malloc(num_of_blocks * sizeof(Block));

    int c = 0;
    for (int i = 0; i < num_of_blocks; i++) {
        Block b;
        for (int j = 0; j < 4; j++) {
            for (int k = 0; k < 4; k++) {
                if (c >= length) {
                    b.data[k][j] = padding;

                } else {
                    b.data[k][j] = data[c];
                    c++;
                }
            }
        }
        blocks[i] = b;
    }
}


void getcol(int i, int col, unsigned char* out) {
    out[0] = blocks[i].data[0][col];
    out[1] = blocks[i].data[1][col];
    out[2] = blocks[i].data[2][col];
    out[3] = blocks[i].data[3][col];
}


void setcol(int i, int col, unsigned char* in) {
    blocks[i].data[0][col] = in[0];
    blocks[i].data[1][col] = in[1];
    blocks[i].data[2][col] = in[2];
    blocks[i].data[3][col] = in[3];
}


void decrypt(unsigned char* in, int length) {
    int rounds = 10;
    unsigned char c1[4];
    unsigned char c2[4];
    unsigned char c3[4];
    unsigned char c4[4];
    int i, j, k;

    expand_key();
    blockify(in, length, false);

    update_key(rounds);
    for (i = 0; i < num_of_blocks; i++) {
        for (j = 0; j < 4; j++) {
            xor_(i, j, key.data[j]);
        }
    }

    while (rounds > 1) {
        for (i = 0; i < num_of_blocks; i++) {
            // Inverse shift rows
            rotate_block_inv(i, 1, 1);
            rotate_block_inv(i, 2, 2);
            rotate_block_inv(i, 3, 3);

            // Inverse sub bytes
            for (j = 0; j < 4; j++) {
                for (k = 0; k < 4; k++) {
                    blocks[i].data[j][k] = 
                    inv_sbox[blocks[i].data[j][k]];
                }
            }
        }
        rounds = rounds - 1;
        update_key(rounds);
        // Add round key
        for (i = 0; i < num_of_blocks; i++)
            for (j = 0; j < 4; j++)
                xor_(i, j, key.data[j]);
        
        for (i = 0; i < num_of_blocks; i++) {
            // Get all columns
            getcol(i, 0, c1);
            getcol(i, 1, c2);
            getcol(i, 2, c3);
            getcol(i, 3, c4);

            // Unmix columns
            gmix_column_inv(c1);
            gmix_column_inv(c2);
            gmix_column_inv(c3);
            gmix_column_inv(c4);

            // Update columns
            setcol(i, 0, c1);
            setcol(i, 1, c2);
            setcol(i, 2, c3);
            setcol(i, 3, c4);
        }
    }
    // Final inverse row shifting and byte subs
    for (i = 0; i < num_of_blocks; i++) {
        rotate_block_inv(i, 1, 1);
        rotate_block_inv(i, 2, 2);
        rotate_block_inv(i, 3, 3);

        for (j = 0; j < 4; j++) {
            for (k = 0; k < 4; k++) {
                blocks[i].data[j][k] = 
                inv_sbox[blocks[i].data[j][k]];
            }
        }
    }
    // Add final round key
    update_key(0);
    for (i = 0; i < num_of_blocks; i++)
        for (j = 0; j < 4; j++)
            xor_(i, j, key.data[j]);
}


void encrypt(unsigned char* in, int length) {
    int rounds = 1;
    unsigned char c1[4];
    unsigned char c2[4];
    unsigned char c3[4];
    unsigned char c4[4];
    int i, j, k;

    expand_key();
    blockify(in, length, true);
    
    // Add round key
    for (i = 0; i < num_of_blocks; i++) {
        for (j = 0; j < 4; j++) {
            xor_(i, j, key.data[j]);
        }
    }
    // Get new key
    update_key(rounds);

    while (rounds <= 9) {
        for (i = 0; i < num_of_blocks; i++) {
            for (j = 0; j < 4; j++) {
                for (k = 0; k < 4; k++) {
                    blocks[i].data[j][k] = s_box[blocks[i].data[j][k]];
                }
            }
        }
        for (i = 0; i < num_of_blocks; i++) {
            // Shift rows
            rotate_block(i, 1, 1);
            rotate_block(i, 2, 2);
            rotate_block(i, 3, 3);

            // Get all columns
            getcol(i, 0, c1);
            getcol(i, 1, c2);
            getcol(i, 2, c3);
            getcol(i, 3, c4);

            // Mix up columns
            gmix_column(c1);
            gmix_column(c2);
            gmix_column(c3);
            gmix_column(c4);

            // Update columns
            setcol(i, 0, c1);
            setcol(i, 1, c2);
            setcol(i, 2, c3);
            setcol(i, 3, c4);
        }

        rounds = rounds + 1;
        // Add round key
        for (i = 0; i < num_of_blocks; i++)
            for (j = 0; j < 4; j++)
                xor_(i, j, key.data[j]);
        // Get new key
        update_key(rounds);
    }

    // Final byte substitution
    for (i = 0; i < num_of_blocks; i++) {
        for (j = 0; j < 4; j++) {
            for (k = 0; k < 4; k++) {
                blocks[i].data[j][k] = 
                s_box[blocks[i].data[j][k]];
            }
        }
    }
    // Final row shifting
    for (i = 0; i < num_of_blocks; i++) {
        rotate_block(i, 1, 1);
        rotate_block(i, 2, 2);
        rotate_block(i, 3, 3);
    }
    // Final round key
    for (i = 0; i < num_of_blocks; i++)
        for (j = 0; j < 4; j++)
            xor_(i, j, key.data[j]);

}


void get_ciphertext(unsigned char* out) {
    int c = 0;
    for (int i = 0; i < num_of_blocks; i++) {
        for (int j = 0; j < 4; j++) {
            for (int k = 0; k < 4; k++) {
                out[c] = blocks[i].data[k][j];
                c++;
            }
        }
    }
    free(blocks);
}